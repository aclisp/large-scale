// 1 str1 > str2
// 0 str1 = str2
// -1 str1 < str2
int pinyin_strncmp_utf8(const void * str1, const void * str2, int n)
{
    const unsigned char* p1 = (const unsigned char*)str1;
    const unsigned char* p2 = (const unsigned char*)str2;

    for (int i=0; i<n; i++)
    {
        if (p1[i] > 0xE0 && p2[i] > 0xE0) // 三个字节编码的情况
        {
            //转换成UNICODE两个字节的编码
            unsigned short code1 = ((p1[i] & 0x0F) <<12)
                + ((p1[i+1]&0x3F) << 6) + (p1[i+2]& 0x3F);
            unsigned short code2 = ((p2[i] & 0x0F) <<12)
                + ((p2[i+1]&0x3F) << 6) + (p2[i+2]& 0x3F);
            if (code1 == code2)
            {
                i += 2;
                continue;
            }
            if (code1 >= 0x4E00 && code2 >= 0x4E00 && 
			   code1 - 0x4E00 < MAX_CHAR && 
			   code2 - 0x4E00 < MAX_CHAR)
            {
                // 转换成拼音比较
                // 这两个汉字都是生僻字，拼音库中没有，不比较
                if (pinyin[code1 - 0x4E00] == 0)
                {
                    if ( pinyin[code2 - 0x4E00] == 0)
                    {
                        i += 2;
                        continue;
                    } 
					else
					{
                        return 1;
					}
                }
                if (pinyin[code2 - 0x4E00] == 0)
                {
                    return -1;
                }
				int rc = strcmp(pinyin[code1 - 0x4E00],pinyin[code2 - 0x4E00]);
				if (rc == 0)
				{
					i += 2;
					continue;
				}
				return rc < 0 ? -1 : 1;
            }
            return code1 < code2 ? -1 : 1;
        }
        //低于三个字节的编码
        if (p1[i] == p2[i])
            continue;
        return p1[i] < p2[i] ? -1 : 1;
    }
    return 0;
}

int pinyin_cmp_utf8(void * ,
        int nKey1, const void * pKey1,
        int nKey2, const void * pKey2)
{
    int n = nKey1 < nKey2 ? nKey1 : nKey2;
    int rc = pinyin_strncmp_utf8(pKey1, pKey2, n);
	if (rc)
		return rc;
	else
		return nKey1-nKey2;
}

